	public boolean generate(World world, Random rand, int x, int y, int z) {
		
		int top = 0;
		
		for (int surface = 7; surface < 30; surface++)
		{
			if (world.isAirBlock(x, y + surface, z))
			{
				top = surface;
				for (int i = -6; i <= 6; i++) {
					for (int j = -6; j <= 6; j++) {
						for (int k = 0; k <= 6; k++)
						{
							world.setBlockToAir(x + i, y + k, z + j);
						}
					}
				}
				generateRealStruc(world, x, y, z);
				generateEntrance(world, x, y, z, top);
				
				return true;
			}
		}		
		return false;
	}
	
	private void generateEntrance(World world, int x, int y, int z, int surface) {
		
		for (int i = 0; i < surface; i++)
		{
			world.setBlockToAir(x, y + surface, z);
			world.setBlock(x + 1, y + surface, z, ZenScape.blockZenBricks, 4, 3);
			world.setBlock(x - 1, y + surface, z, ZenScape.blockZenBricks, 4, 3);
			world.setBlock(x, y + surface, z + 1, ZenScape.blockZenBricks, 4, 3);
			world.setBlock(x, y + surface, z - 1, ZenScape.blockZenBricks, 4, 3);
		}
	}
	
